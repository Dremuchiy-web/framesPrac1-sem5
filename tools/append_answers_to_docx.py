def main() -> None:
    docx_path = find_docx()

    # Some provided "docx" files can be non-zip containers; python-docx can't edit them.
    # In that case we regenerate a valid .docx with the same name.
    if zipfile.is_zipfile(docx_path):
        doc = Document(str(docx_path))
    else:
        doc = Document()
        doc.add_heading(docx_path.stem, level=1)
        for line in BASE_TEXT:
            doc.add_paragraph(line)

    doc.add_page_break()
    doc.add_heading("Ответы на контрольные вопросы (для защиты)", level=1)

    for title, bullets in ANSWERS:
        doc.add_paragraph(title)
        for b in bullets:
            doc.add_paragraph(b, style="List Bullet")

    doc.add_paragraph()
    doc.add_paragraph("Примечание: ответы опираются на реализованный конвейер middleware, единый формат ошибок и in-memory репозиторий в проекте.")

    doc.save(str(docx_path))


if __name__ == "__main__":
    main()

